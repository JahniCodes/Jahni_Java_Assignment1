public class Chap3_exer_317DoB {
    private int month;
    private int day;
    private int year;

    public Chap3_exer_317DoB(int month, int day, int year) {
        this.month = month;
        this.day = day;
        this.year = year;
    }

    //GETTERS
    public int getMonth() {
        return this.month;
    }

    public int getDay() {
        return this.day;
    }

    public int getYear() {
        return this.year;
    }

    //SETTERS
    public void setMonth(int month){
        this.month = month;
    }
    public void setDay(int day){
        this.day = day;
    }
    public void setYear(int year){
        this.year = year;
    }
}
